package com.javaex.ex01;

public class Ex04 {
	public static void main(String[] args) {
		
		double pi = 3.14;
		
		System.out.println(pi);
		
		pi=5;
		
		System.out.println(pi);
		
		final double PI = 3.14; // final 상수로서 값을 변경할 수 없고. 변수명을 대문자로 분류한다.
		
		System.out.println(PI);
		
	}
}
